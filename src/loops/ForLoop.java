package loops;

public class ForLoop {
    //condition in the same line.
    public static void main(String[] args) {
        int i;
        for (i = 1; i < 50; i++) {
            System.out.println(i);
        }
        //practice : print 0 to 10

        int a ;
        //initialization, condition and increment or decrement in the same line.
        for (a = 0; a<=10; a++){
            System.out.println(a);
        }
        //Print 10 to 1
        // use decrement for descending order
        int j;
        for (j = 10; j>=1;j--){
            System.out.println(j);
        }


    }

}
