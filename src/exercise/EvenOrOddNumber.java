package exercise;

public class EvenOrOddNumber {
    //number that is divided by 2 is even number, remainder is 0
    //if the number is not divided by 2 and remainder is 1 that is odd number
    public static void main(String[] args){
        int number = 79;
        if(number/2 == 0){
            System.out.println("it is even number");
        }else{
            System.out.println("it is odd number");
        }
    }
}
