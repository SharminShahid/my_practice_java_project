package methods;

public class MethodWithoutParameter {
    //Method is reusable a block of code which runs only when it is called.
    //we can pass data known as parameters inside a method.
    //Method is important to reuse code.
    //Static method means it belong to the main class.
    // void means the method will return no value.

    //creating a method
    public static void myMethod(){
        System.out.println("My name is john");
    }
    //calling the method
    public static void main(String[] args){
        myMethod();
    }
}
